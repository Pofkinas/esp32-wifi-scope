/* clang-format off */
static const sGpioDesc_t g_static_gpio_lut[eGpio_Last] = {
    [eGpio_Led] = {
        .gpio_num = GPIO_NUM_4,
        .mode = GPIO_MODE_OUTPUT,
        .pull_up = GPIO_PULLUP_DISABLE,
        .pull_down = GPIO_PULLDOWN_DISABLE,
    },
    [eGpio_Button] = {
        .gpio_num = GPIO_NUM_21,
        .mode = GPIO_MODE_INPUT,
        .pull_up = GPIO_PULLUP_DISABLE,
        .pull_down = GPIO_PULLDOWN_DISABLE,
    }
};
