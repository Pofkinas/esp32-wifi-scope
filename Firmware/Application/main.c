/**********************************************************************************************************************
 * Definitions of private functions
 *********************************************************************************************************************/

static void Main_Init(void) {
    bool init_success = true;

    if (!CLI_APP_Init(UART_0_BAUDRATE)) {
        init_success = false;
    }

    if (!Oscilloscope_APP_Init()) {
        init_success = false;
    }

    if (!init_success) {
        while (1) {}
    }

    TRACE_INFO("Start OK\n");

    return;
}

/**********************************************************************************************************************
 * Definitions of exported functions
 *********************************************************************************************************************/

int app_main(void) {
    Main_Init();

    vTaskDelete(NULL);

    return 0;
}
